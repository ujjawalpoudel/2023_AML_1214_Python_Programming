"""
This assigment generate square shape graphic using turtle builtin package of python.
"""

# * Import Python Package
import turtle

# * Define colour for graphics
turtle.color("blue")

# * Define size of graphics
turtle.pensize(10)

# * Move graphics to specified position
turtle.forward(100)

# * Move graphics to specified angle direction
turtle.left(90)

# * Move graphics to specified position
turtle.forward(100)

# * Move graphics to specified angle direction
turtle.left(90)

# * Move graphics to specified position
turtle.forward(100)

# * Move graphics to specified angle direction
turtle.left(90)

# * Move graphics to specified position
turtle.forward(100)

# * Move graphics to specified angle direction
turtle.left(90)

# * End the graphics
turtle.done()
